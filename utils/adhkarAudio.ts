/**
 * Get the audio source for a dhikr by its audio filename
 * @param audioFile - The audio filename (e.g., "adhkar_75.mp3")
 * @returns The require() source or null if not found
 */
export function getAudioSource(audioFile: string | null): number | null {
  if (!audioFile) return null;
  return audioAssets[audioFile] ?? null;
}

/**
 * Check if audio exists for a given filename
 * @param audioFile - The audio filename (e.g., "adhkar_75.mp3")
 * @returns True if the audio file exists in the mapping
 */
export function hasAudio(audioFile: string | null): boolean {
  if (!audioFile) return false;
  return audioFile in audioAssets;
}

/**
 * Get all available audio filenames
 * @returns Array of all audio filenames
 */
export function getAllAudioFiles(): string[] {
  return Object.keys(audioAssets);
}

/**
 * Get the total count of available audio files
 * @returns Number of audio files in the mapping
 */
export function getAudioCount(): number {
  return Object.keys(audioAssets).length;
}
